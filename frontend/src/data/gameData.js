export function getChampionImage(champKey) {
  if (!champKey) return PLACEHOLDER_IMG;
  const champLower = champKey.toLowerCase().replace(/[^a-z]/g, "");
  const setVer = currentVersion || DD_VERSION;
  return `${TFT_CHAMPIONS_URL}tft${setVer}_${champLower}_square.tft_set${setVer}.png`;
}

