export function getChampionImage(champKey) {
  if (!champKey) return null;
  const key = champKey
    .toLowerCase()
    .replace(/\s/g, "")
    .replace("'", "")
    .replace(".", "");
  return `/images/champions/${key}.png`;
}

export function getItemImage(itemId) {
  if (!itemId) return null;
  return `/images/items/${itemId}.png`;
}

export function getTraitImage(traitKey) {
  if (!traitKey) return null;
  const key = traitKey
    .toLowerCase()
    .replace(/\s/g, "")
    .replace("'", "")
    .replace("-", "");
  return `/images/traits/${key}.png`;
}

export function getItemIconFallback(itemKey) {
  const colors = {
    Rabadons: "#A335EE",
    HoJ: "#FFD700",
    BT: "#C0C0C0",
  };
  return colors[itemKey] || "#95A4A4";
}
