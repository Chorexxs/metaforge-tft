export const DD_VERSION = "17";
let currentVersion = DD_VERSION;
let versionDetected = false;
let detectPromise = null;

export function getCurrentVersion() {
  return currentVersion;
}

const TFT_CHAMPIONS_URL =
  "https://raw.communitydragon.org/latest/game/assets/ux/tft/championsplashes/";
const TFT_ITEMS_URL =
  "https://raw.communitydragon.org/latest/game/assets/maps/particles/tft/item_icons/";
const TFT_TRAITS_URL =
  "https://raw.communitydragon.org/latest/game/assets/maps/particles/tft/traits/";

const CDN_CDRAGON = (path) =>
  `https://raw.communitydragon.org/latest/plugins/rcp-be-lol-game-data/global/default/v1/${path}`;

const CDN_CDRAGON_TFT = (path) =>
  `https://raw.communitydragon.org/latest/game/assets/maps/particles/tft/${path}`;

const CDN_DDRAGON = (path) =>
  `https://ddragon.leagueoflegends.com/cdn/${currentVersion}/img/${path}`;

async function detectTFTSetVersion() {
  if (versionDetected && currentVersion !== DD_VERSION) {
    return currentVersion;
  }

  if (detectPromise) {
    return detectPromise;
  }

  detectPromise = (async () => {
    try {
      const response = await fetch(TFT_CHAMPIONS_URL);
      const html = await response.text();

      const regex = /tft(\d+)_[a-z]+_square\.tft_set\d+\.png/gi;
      let match;
      let maxSet = 0;

      while ((match = regex.exec(html)) !== null) {
        const setNum = parseInt(match[1], 10);
        if (setNum > maxSet) {
          maxSet = setNum;
        }
      }

      if (maxSet > 0) {
        currentVersion = String(maxSet);
        versionDetected = true;
        console.log(`[TFT HUD] Detected TFT Set ${currentVersion}`);
      }
    } catch (e) {
      console.warn(
        "[TFT HUD] Failed to detect TFT version, using default:",
        DD_VERSION,
      );
    }

    return currentVersion;
  })();

  return detectPromise;
}

export async function initializeVersion() {
  return detectTFTSetVersion();
}

